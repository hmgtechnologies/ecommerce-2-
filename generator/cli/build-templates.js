#!/usr/bin/env node
/* ============================================================================
 * HMG StoreForge — Template Embedder
 * ----------------------------------------------------------------------------
 * Re-embeds the store-template files into generator/assets/js/templates.js so
 * the BROWSER generator stays in sync with the store-template folder.
 * Run this whenever you edit anything in store-template/.
 *
 * USAGE:  node build-templates.js
 * (The CLI generator reads store-template directly and never needs this.)
 * ==========================================================================*/
const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "..", "..");          // e-commerce/
const ST = path.join(ROOT, "store-template");
const OUT = path.join(ROOT, "generator", "assets", "js", "templates.js");

const TEXT = [
  "index.html", "admin.html", "track.html", "vendor-apply.html", "vendor.html", "dispatch.html", "ledger.html",
  "products.json", "manifest.webmanifest", "sw.js",
  "assets/css/style.css", "assets/css/extras.css", "assets/css/themes.css",
  "assets/js/i18n.js", "assets/js/theme-engine.js",
  "assets/js/store.js", "assets/js/store-extras.js", "assets/js/store-extras2.js",
  "assets/js/store-extras3.js", "assets/js/store-extras4.js", "assets/js/store-extras5.js", "assets/js/store-extras6.js", "assets/js/store-extras7.js", "assets/js/store-extras8.js",
  "assets/js/marketplace.js",
  "assets/js/analytics.js", "assets/js/products-fallback.js",
];
const BIN = [
  "assets/images/placeholder.png", "assets/images/hero.jpg",
  "assets/images/favicon.png", "assets/images/og-image.jpg",
  "assets/images/logo.png",
];

const templates = {};
for (const f of TEXT) templates[f] = fs.readFileSync(path.join(ST, f), "utf8");

const binaries = {};
for (const f of BIN) {
  const p = path.join(ST, f);
  if (fs.existsSync(p)) binaries[f] = fs.readFileSync(p).toString("base64");
}

const out =
  "/* AUTO-GENERATED by build-templates.js — do not edit by hand. */\n" +
  "window.SF_TEMPLATES = " + JSON.stringify(templates) + ";\n" +
  "window.SF_BINARIES = " + JSON.stringify(binaries) + ";\n";

fs.writeFileSync(OUT, out);
console.log("✅ Rebuilt templates.js (" + Math.round(out.length / 1024) + " KB)");
console.log("   text files:", TEXT.length, "· binary files:", Object.keys(binaries).length);
